CREATE TABLE user (
  id INT NOT NULL AUTO_INCREMENT,
  email VARCHAR(100) UNIQUE NOT NULL,
  password char(64) NOT NULL,
  username VARCHAR(40) NOT NULL,
  handle VARCHAR(40) UNIQUE NOT NULL,
  bio VARCHAR(160),
  theme_bg INT NOT NULL DEFAULT 0,
  theme_accent INT NOT NULL DEFAULT 0,
  PRIMARY KEY (id)
);

CREATE TABLE chirp (
  id INT NOT NULL AUTO_INCREMENT,
  timestamp DATETIME NOT NULL,
  text VARCHAR(400) NOT NULL,
  image BOOLEAN,
  author_id INT NOT NULL,
  reply_to_id INT,
  PRIMARY KEY(id),
  FOREIGN KEY (author_id) REFERENCES user(id) ON DELETE CASCADE,
  FOREIGN KEY (reply_to_id) REFERENCES chirp(id) ON DELETE SET NULL
);

-- Join table for n..m relationship
CREATE TABLE user_stars_chirp (
  user_id INT NOT NULL,
  chirp_id INT NOT NULL,
  PRIMARY KEY (user_id, chirp_id),
  FOREIGN KEY (user_id) REFERENCES user(id) ON DELETE CASCADE ON UPDATE RESTRICT,
  FOREIGN KEY (chirp_id) REFERENCES chirp(id) ON DELETE CASCADE ON UPDATE RESTRICT
);

--
-- Create views
--
CREATE VIEW chirp_star_count_vw AS (
	SELECT chirp.id, COUNT(user_stars_chirp.chirp_id) AS star_count FROM chirp LEFT JOIN user_stars_chirp ON chirp.id = user_stars_chirp.chirp_id GROUP BY chirp.id
);

CREATE VIEW chirp_reply_count_vw AS (
	SELECT main_chirp.id, COUNT(reply_chirp.reply_to_id) AS reply_count FROM chirp main_chirp LEFT JOIN chirp reply_chirp ON main_chirp.id = reply_chirp.reply_to_id GROUP BY main_chirp.id
);


